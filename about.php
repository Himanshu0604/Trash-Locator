    <h3 style="text-align:left">ABOUT THE APPLICATION</h3>
    <p>
    Civic authorities in many Indian cities have a tough time in garbage collection and as a result there is a pile up of garbage in the cities. In this project, we address the problem of real-time detection of garbage and the process of garbage management using a two step approach. In the first step, we build a web application that allows drone, CCTVs and citizens to capture images of garbage and upload them to our server. In the second step, back-end performs analysis on these images to estimate the area of garbage using Mask R-CNN. Our approach is to detect the Real time Geo-Mapped trash locations and perform area estimation and update them in the database and across all interactive systems thus alerting the authorities concerned.
    


